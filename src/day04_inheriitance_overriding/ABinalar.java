package day04_inheriitance_overriding;

public class ABinalar {

       protected void temel () {
           System.out.println("Butun binalarin temeli vardir");
       }
       protected void cati(){
           System.out.println("Butun binalarin catisi vardir");
       }
       protected void kapi() {
           System.out.println("Butun binalarin kapisi vardir");
       }
       protected void pencere(){
           System.out.println("Butun binalarin temeli vardir");
       }

}
